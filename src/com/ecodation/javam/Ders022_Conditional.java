package com.ecodation.javam;

public class Ders022_Conditional {
	
	public static void main(String[] args) {
		
		int sayi = 5;
		
		if (sayi > 10) {
			System.out.println("sayi 10'dan büyüktür.");
		} else if (sayi > 8) {
			System.out.println("sayi 8'den büyüktür");
		} else if (sayi > 6) {
			System.out.println("sayi 6'den büyüktür");
		} else if (sayi > 5) {
			System.out.println("sayi 5'den büyüktür");
		} else {
			System.out.println("sayi 4'den büyüktür");
		}
		
	}
	
}
