package com.ecodation.javam;

public class Ders025_Conditional4 {
	
	public static void main(String[] args) {
		
		int sayi = 45;
		
		switch (sayi) {
			case 2:
				System.out.println("Sayı 2 ");
				break;
			
			case 1:
				System.out.println("Sayı 1 ");
				break;
			
			case 3:
				System.out.println("Sayı 3 ");
				break;
			
			default:
				System.out.println("sayı bu aralıkta değildir");
				break;
		}
		
	}
	
}
