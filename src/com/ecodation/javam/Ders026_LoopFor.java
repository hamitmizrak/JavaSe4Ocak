package com.ecodation.javam;

public class Ders026_LoopFor {
	
	public static void main(String[] args) {
		
		/*
		 * for (int i = 3; i <= 12; i++) { System.out.print(i + " "); }
		 */
		
		/*
		 * int k; for (k = 5; k <= 10; k++) { System.out.print(k + " "); }
		 */
		
		/*
		 * for (int i = 0; i < 10; i += 1) { System.out.print(i + " "); }
		 */
		
		for (int i = 0; i < 10; i += 2) {
			System.out.print(i + " ");
		}
		System.out.println("\n----------");
		for (int i = 1; i < 10; i += 2) {
			System.out.print(i + " ");
		}
		
	}
	
}
